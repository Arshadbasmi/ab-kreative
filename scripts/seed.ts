async function main() {
  console.log('Seeding AB Kreative database...')

  // Clear existing data
  await db.lead.deleteMany()

  // Insert leads
  console.log(`Inserting ${leads.length} leads...`)
  for (const lead of leads) {
    await db.lead.create({ data: lead })
  }

  // Summary
  const counts = await db.lead.groupBy({
    by: ['category'],
    _count: { _all: true },
  })

  console.log('Seed completed successfully!')
  console.log(`   - ${leads.length} total leads across 6 categories:`)
  for (const c of counts) {
    console.log(`     ${c.category}: ${c._count._all} leads`)
  }
}

main()
  .catch((e) => {
    console.error('Seed failed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await db.$disconnect()
  })